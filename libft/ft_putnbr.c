#include "libft.h"

void	ft_putnbr(int c)
{
	if (c == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		c = c % 2000000000;
		c *= -1;
	}
	if (c < 0)
	{
		ft_putchar('-');
		c *= -1;
	}
	if (c > 9)
		ft_putnbr(c / 10);
	ft_putchar(c % 10 + '0');
}
