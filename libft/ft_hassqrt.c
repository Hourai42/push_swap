#include "libft.h"

int	ft_hassqrt(int nb)
{
	int i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (nb);
	while (i < nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (-1);
}
