#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	size_t	i;
	size_t	z;

	z = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[z] != '\0' && n != 0)
	{
		s1[i] = s2[z];
		i++;
		z++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
