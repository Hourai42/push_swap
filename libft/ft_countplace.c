#include "libft.h"

int	ft_countplace(int n)
{
	int count;

	count = 0;
	if (n < 0)
		n *= -1;
	if (n == 0)
		count++;
	while (n != 0)
	{
		count++;
		n /= 10;
	}
	return (count);
}
